    public static String showEditString(Component parent, String title, String message, Icon icon, String sValue) {

            Window window = SwingUtilities.windowForComponent(parent);

            JStringDialog myMsg;
            if (window instanceof Frame) { 
                myMsg = new JStringDialog((Frame) window, true);
            } else {
                myMsg = new JStringDialog((Dialog) window, true);
            }

            myMsg.setModalityType(Dialog.ModalityType.APPLICATION_MODAL);
            myMsg.setTitle(title, message, icon);
            myMsg.setValue(sValue);
            myMsg.setVisible(true);

            return myMsg.m_value;
    }
    
