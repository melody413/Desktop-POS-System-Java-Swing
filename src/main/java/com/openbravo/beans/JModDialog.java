    public static JModDialog newJDialog(Window window) {
        
//        Window window = getWindow(ticketsbagshared);
        JModDialog mydialog;
        if (window instanceof Frame) { 
            mydialog = new JModDialog((Frame) window, true);
        } else {
            mydialog = new JModDialog((Dialog) window, true);
        } 
 
        mydialog.initComponents();

        if (m_resources == null) {
            m_resources = new LocaleResources();
            m_resources.addBundleName("beans_messages");
        }
        
        mydialog.jScrollPane1.getVerticalScrollBar().setPreferredSize(new Dimension(35, 35));
        mydialog.jScrollPane1.getHorizontalScrollBar().setPreferredSize(new Dimension(25, 25));
        mydialog.setTitle("Mod");
        mydialog.m_jButtonOK.setText("OK");
        
        return mydialog;
    }
    
    private static Window getWindow(Component parent) {
        if (parent == null) {
            return new JFrame();
        } else if (parent instanceof Frame || parent instanceof Dialog) {
            return (Window) parent;
        } else {
            return getWindow(parent.getParent());
        }
    }  

